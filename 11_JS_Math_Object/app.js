// Square root
let sqrt = Math.sqrt(144);
console.log(`Square root of 144 is: ${sqrt}`);

// Power
let pow = Math.pow(2, 5);
console.log(`2 ^ 5 is: ${pow}`);

// min
let min = Math.min(1, 2, 3, 4, 5);
console.log(`Min is: ${min}`);

// max
let max = Math.max(1, 2, 3, 4, 5);
console.log(`Max is: ${max}`);

// Generate random numbers with round function
let random = Math.round(Math.random() * 100000);
console.log(`Random Number is: ${random}`);